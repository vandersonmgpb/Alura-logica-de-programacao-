//  Criar uma função que recebe dois números como parâmetros e retorna o maior deles.

function maiorNumero(a, b) {
    return a > b ? a : b;
}
let resultado = maiorNumero(10, 7);
console.log("O maior número é: " + resultado); // Exibe: O maior número é: 10
